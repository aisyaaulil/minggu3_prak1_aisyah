       <h3>Kantin</h3>
        <p> 
         Kantin merupakan salah satu tempat bagi anak Jurusan Teknologi Informasi untuk membeli makanan dan minuman pada jam istirahat. <br>
         Kantin ini menyediakan berbagai macam makanan mulai dari snack ringan, nasi jagung, nasi kepal, roti, hingga mie gelas.
        </p>
        <h3>Ruang Baca</h3>
        <p> 
        Ruang baca Jurusan Teknologi Informasi merupakan tempat untu mahasiswa Jurusan Teknologi Informasi membaca<br>
        maupun melakukan aktivitas seperti kerja kelompok. Ruang baca ini terletak di Gdeung AF lantai 3. Fasilitas<br>
        yang ada pada ruangan ini terbilang cukup lengkap.
        </p>
        <h3>Ruang Admin</h3>
        <p> 
        Ruangan ini adalah ruangan yang digunakan untuk tempat administrasi Jurusan untuk prodi D3 dan D4. Disini<br>
        adalah tempat para mahasiswa untuk mengambil absensi, memberikan surat sakit, mengurus surat dispensasi, dan juga<br>
        mengurus kompensasi alpa.
        </p>
